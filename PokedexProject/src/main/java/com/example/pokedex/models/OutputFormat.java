package com.example.pokedex.models;

/**
 * OutputFormat enum represents different formats for outputting data, including TEXT, HTML, and CSV.
 */
public enum OutputFormat {

    /**
     * TEXT format for plain text output.
     */
    TEXT,

    /**
     * HTML format for output in HTML markup.
     */
    HTML,

    /**
     * CSV format for output in Comma-Separated Values format.
     */
    CSV
}

